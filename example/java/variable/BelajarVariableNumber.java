package example.java.variable;

public class BelajarVariableNumber{

    public static void main(String[] args) {
        Integer var1 = 1000;
        Short ram = 127;
        Double pecahanDouble = 10000.5;
        Float pecahanFloat = 10.0f;
        Long rupiah = 100_000_000l;
        System.out.println("nilai berikut: ");
        System.out.println("var1 = "+ var1);
        System.out.println("ram = "+ ram);
        System.out.println("pecahanDouble = "+ pecahanDouble);
        System.out.println("pecahanFloat = "+ pecahanFloat);
        System.out.println("rupiah = "+ rupiah);
    }
}